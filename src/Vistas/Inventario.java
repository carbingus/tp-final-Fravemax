    private void generarTablin(){
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("ID");
        columnas.add("Nombre");
        columnas.add("Categoria");
        columnas.add("Price");
        columnas.add("Stock");
        columnas.add("Estado");

        for (Object columna : columnas) {
            tablin.addColumn(columna);
        }
        jtblProductos.setModel(tablin); 

        limpiarTablin();
        accederTablin();
    }
    
    private void limpiarTablin(){
        int fila = tablin.getRowCount() - 1;
        
        for (int i = fila; i >= 0; i--){
            tablin.removeRow(i);
        }
    }
    
    private void accederTablin(){
        System.out.println("generando tablin");
         listaProductos = (ArrayList<Producto>) prodData.listarProductos();
         System.out.println("Product count: " + listaProductos.size());

        for (Producto prod : listaProductos) {

            String estado;
            if (prod.getEstado()) {
                estado = "Disponible";
            } else {
                estado = "No disponible";
            }

            tablin.addRow(new Object[]{
                prod.getIdProducto(), 
                prod.getNombre(), 
                prod.getCategoria(), 
                prod.getPrecio(), 
                prod.getStock(), 
                estado
            });
        }
    }

